local dt = require("jsonata.functions.datetime")
local cal = dt._internal.calendar

describe("M8c-1: calendar millis<->components", function()
  it("epoch 0 = 1970-01-01 Thursday", function()
    local c = cal.millis_to_components(0)
    assert.are.same({ year = 1970, month0 = 0, day = 1, hours = 0, minutes = 0, seconds = 0, ms = 0, weekday = 4 }, c)
  end)
  it("known timestamp 1521801216617 = 2018-03-23T10:33:36.617 (Friday)", function()
    local c = cal.millis_to_components(1521801216617)
    assert.are.equal(2018, c.year)
    assert.are.equal(2, c.month0)
    assert.are.equal(23, c.day)
    assert.are.equal(10, c.hours)
    assert.are.equal(33, c.minutes)
    assert.are.equal(36, c.seconds)
    assert.are.equal(617, c.ms)
    assert.are.equal(5, c.weekday)
  end)
  it("components_to_millis inverts", function()
    assert.are.equal(0, cal.components_to_millis(1970, 0, 1, 0, 0, 0, 0))
    assert.are.equal(1521801216617, cal.components_to_millis(2018, 2, 23, 10, 33, 36, 617))
    assert.are.equal(1, cal.components_to_millis(1970, 0, 1, 0, 0, 0, 1))
  end)
  it("pre-epoch / BCE round-trips (1881-12-21 negative millis)", function()
    local ms = cal.components_to_millis(1881, 11, 21, 0, 0, 0, 0)
    assert.are.equal(-2777932800000, ms)
    local c = cal.millis_to_components(ms)
    assert.are.equal(1881, c.year)
    assert.are.equal(11, c.month0)
    assert.are.equal(21, c.day)
  end)
  it("leap day 2020-02-29 round-trips", function()
    local ms = cal.components_to_millis(2020, 1, 29, 12, 0, 0, 0)
    local c = cal.millis_to_components(ms)
    assert.are.equal(2020, c.year)
    assert.are.equal(1, c.month0)
    assert.are.equal(29, c.day)
    assert.are.equal(12, c.hours)
  end)
  it("pre-epoch weekday: 1969-12-31 is Wednesday(3)", function()
    local c = cal.millis_to_components(-86400000)
    assert.are.equal(1969, c.year)
    assert.are.equal(11, c.month0)
    assert.are.equal(31, c.day)
    assert.are.equal(3, c.weekday)
  end)
  it("negative-year (BCE) round-trips match JS Date", function()
    -- node: new Date(-123456789012345) = -1943-10-22T23:29:47.655Z (month0=9, day=22)
    local c = cal.millis_to_components(-123456789012345)
    assert.are.equal(-1943, c.year)
    assert.are.equal(9, c.month0)
    assert.are.equal(22, c.day)
    assert.are.equal(23, c.hours)
    assert.are.equal(29, c.minutes)
    assert.are.equal(47, c.seconds)
    assert.are.equal(655, c.ms)
    assert.are.equal(-123456789012345, cal.components_to_millis(-1943, 9, 22, 23, 29, 47, 655))
    -- node: Date.UTC(-9999,0,1) = -377705116800000
    assert.are.equal(-377705116800000, cal.components_to_millis(-9999, 0, 1, 0, 0, 0, 0))
    local c2 = cal.millis_to_components(-377705116800000)
    assert.are.equal(-9999, c2.year)
    assert.are.equal(0, c2.month0)
    assert.are.equal(1, c2.day)
  end)
end)

describe("M8c-1: get_datetime_fragment", function()
  local frag = dt._internal.get_datetime_fragment
  local c = cal.millis_to_components(1521801216617) -- 2018-03-23T10:33:36.617 Fri
  it("basic components", function()
    assert.are.equal(2018, frag(c, "Y"))
    assert.are.equal(3, frag(c, "M"))
    assert.are.equal(23, frag(c, "D"))
    assert.are.equal(10, frag(c, "H"))
    assert.are.equal(33, frag(c, "m"))
    assert.are.equal(36, frag(c, "s"))
    assert.are.equal(617, frag(c, "f"))
    assert.are.equal(5, frag(c, "F"))
    assert.are.equal(10, frag(c, "h"))
    assert.are.equal("am", frag(c, "P"))
    assert.are.equal(82, frag(c, "d"))
  end)
  it("12h + pm", function()
    local pm = cal.millis_to_components(cal.components_to_millis(2018, 0, 1, 13, 0, 0, 0))
    assert.are.equal(1, frag(pm, "h"))
    assert.are.equal("pm", frag(pm, "P"))
    local mid = cal.millis_to_components(cal.components_to_millis(2018, 0, 1, 0, 0, 0, 0))
    assert.are.equal(12, frag(mid, "h"))
  end)
end)

describe("M8c-1: ISO week math (literal millis, no toMillis)", function()
  local frag = dt._internal.get_datetime_fragment
  local function wk(ms)
    local c = cal.millis_to_components(ms)
    return frag(c, "X"), frag(c, "W"), frag(c, "F")
  end
  it("ISO week boundaries", function()
    local x, w, f = wk(1104537600000)
    assert.are.same({ 2004, 53, 6 }, { x, w, f }) -- 2005-01-01
    x, w, f = wk(1136160000000)
    assert.are.same({ 2006, 1, 1 }, { x, w, f }) -- 2006-01-02
    x, w, f = wk(1167609600000)
    assert.are.same({ 2007, 1, 1 }, { x, w, f }) -- 2007-01-01
    x, w, f = wk(1199059200000)
    assert.are.same({ 2008, 1, 1 }, { x, w, f }) -- 2007-12-31
    x, w, f = wk(1230508800000)
    assert.are.same({ 2009, 1, 1 }, { x, w, f }) -- 2008-12-29
    x, w, f = wk(1262217600000)
    assert.are.same({ 2009, 53, 4 }, { x, w, f }) -- 2009-12-31
    x, w, f = wk(1262476800000)
    assert.are.same({ 2009, 53, 7 }, { x, w, f }) -- 2010-01-03
  end)
end)

local jsonata = require("jsonata")
local function run(src, input)
  return jsonata.compile(src):evaluate(input)
end

describe("M8c-1: $fromMillis format", function()
  local ms = 1521801216617 -- 2018-03-23T10:33:36.617Z
  it("default ISO", function()
    assert.are.equal("2018-03-23T10:33:36.617Z", run("$fromMillis(" .. ms .. ")"))
    assert.are.equal("1970-01-01T00:00:00.001Z", run("$fromMillis(1)"))
    assert.are.equal("2017-10-30T16:25:32.935Z", run("$fromMillis(1509380732935)"))
  end)
  it("markers + literals + escaping", function()
    assert.are.equal("Year: 2018", run("$fromMillis(" .. ms .. ", 'Year: [Y0001]')"))
    assert.are.equal("Year: <2018>", run("$fromMillis(" .. ms .. ", 'Year: <[Y0001]>')"))
    assert.are.equal("Year: <2,018>", run("$fromMillis(" .. ms .. ", 'Year: <[Y9,999,*]>')"))
    assert.are.equal("[Year: <2018>", run("$fromMillis(" .. ms .. ", '[[Year: <[Y0001]>')"))
    assert.are.equal("[Year]: [2018]", run("$fromMillis(" .. ms .. ", '[[Year]]: [[[Y0001]]]')"))
    assert.are.equal("23/3/2018", run("$fromMillis(" .. ms .. ", '[D#1]/[M#1]/[Y0001]')"))
    assert.are.equal("2018-03-23", run("$fromMillis(" .. ms .. ", '[Y0001]-[M01]-[D01]')"))
  end)
  it("names", function()
    assert.are.equal("7 Sunday", run("$fromMillis(1522616700000, '[F0] [FNn]')"))
    assert.are.equal("1 Monday", run("$fromMillis(1522703100000, '[F0] [FNn]')"))
    assert.are.equal("03/23/2018 at 10:33:36", run("$fromMillis(" .. ms .. ", '[M01]/[D01]/[Y0001] at [H01]:[m01]:[s01]')"))
  end)
  it("timezones", function()
    assert.are.equal("2018-03-23T11:33:36.617+0100", run("$fromMillis(" .. ms .. ", '[Y]-[M01]-[D01]T[H01]:[m]:[s].[f001][Z0101]', '+0100')"))
    assert.are.equal("2018-07-11T12:00:00+00:00", run("$fromMillis(1531310400000, '[Y]-[M01]-[D01]T[H01]:[m]:[s][Z01:01]')"))
    assert.are.equal("2018-07-11T12:00:00Z", run("$fromMillis(1531310400000, '[Y]-[M01]-[D01]T[H01]:[m]:[s][Z01:01t]')"))
    assert.are.equal("2018-07-11T07:00:00-05:00", run("$fromMillis(1531310400000, '[Y]-[M01]-[D01]T[H01]:[m]:[s][Z]', '-0500')"))
  end)
  it("whitespace in markers ignored", function()
    assert.are.equal("2018-03-23T10:33:36.617Z", run("$fromMillis(" .. ms .. ", '[Y]-[ M01]-[D 01]T[H01 ]:[ m   ]:[s].[f001][Z01:01t]')"))
  end)
  it("ISO week via fromMillis (literal millis)", function()
    assert.are.equal("2004-W53-6", run("$fromMillis(1104537600000, '[X0001]-W[W01]-[F0]')"))
    assert.are.equal("2009-W53-7", run("$fromMillis(1262476800000, '[X0001]-W[W01]-[F0]')"))
  end)
  it("errors + undefined", function()
    assert.is_nil(run("$fromMillis(blah, '[Y]')", {}))
    local ok, err = pcall(run, "$fromMillis(0, '[Q]')")
    assert.is_false(ok)
    assert.are.equal("D3132", err.code)
    local ok2, err2 = pcall(run, "$fromMillis(0, '[Y0001')")
    assert.is_false(ok2)
    assert.are.equal("D3135", err2.code)
  end)
  it("negative timezone offset with minutes (faithful to jsonata floor+trunc)", function()
    assert.are.equal("05:30 -06:30", run("$fromMillis(1531310400000, '[H01]:[m01] [Z]', '-0530')"))
    assert.are.equal("10:15 -01:45", run("$fromMillis(1531310400000, '[H01]:[m01] [Z]', '-0045')"))
    -- positive + zero-minute unaffected
    assert.are.equal("17:30 +05:30", run("$fromMillis(1531310400000, '[H01]:[m01] [Z]', '+0530')"))
  end)
  it("malformed width modifier degrades (no crash), matching oracle", function()
    assert.are.equal("18", run("$fromMillis(1521801216617, '[Y01,]')"))
    assert.are.equal("03", run("$fromMillis(1521801216617, '[M01,]')"))
    assert.are.equal("18", run("$fromMillis(1521801216617, '[Y01,-2]')"))
    assert.are.equal("18", run("$fromMillis(1521801216617, '[Y01,x]')"))
  end)
  it("non-decimal year/component formats (roman/letters/words) - no crash", function()
    assert.are.equal("MMXVIII", run("$fromMillis(1521801216617, '[YI]')"))
    assert.are.equal("mmxviii", run("$fromMillis(1521801216617, '[Yi]')"))
    assert.are.equal("two thousand and eighteen", run("$fromMillis(1521801216617, '[Yw]')"))
    assert.are.equal("23 03 MMXVIII", run("$fromMillis(1521801216617, '[D1] [M01] [YI]')"))
    assert.are.equal("w C mmxviii", run("$fromMillis(1521801216617, '[Da] [MA] [Yi]')"))
  end)
end)

describe("M8c-2: $toMillis ISO-8601 default parse", function()
  it("ISO timestamps", function()
    assert.are.equal(1, run('$toMillis("1970-01-01T00:00:00.001Z")'))
    assert.are.equal(1517478133123, run('$toMillis("2018-02-01T09:42:13.123+0000")'))
    assert.are.equal(1509321600000, run('$toMillis("2017-10-30")'))
    assert.are.equal(1509377314000, run('$toMillis("2017-10-30T15:28:34Z")'))
    assert.are.equal(1514764800000, run('$toMillis("2018")'))
  end)
  it("undefined -> undefined", function()
    assert.is_nil(run("$toMillis(blah)", {}))
  end)
  it("non-ISO without picture -> D3110", function()
    for _, ts in ipairs({ "foo", "01-02-2018", "2018-02-03 11:15:33" }) do
      local ok, err = pcall(run, '$toMillis("' .. ts .. '")')
      assert.is_false(ok)
      assert.are.equal("D3110", err.code)
    end
  end)
end)

describe("M8c-2: $toMillis picture parse", function()
  it("numeric date pictures", function()
    assert.are.equal(1514764800000, run("$toMillis('2018', '[Y1]')"))
    assert.are.equal(1522108800000, run("$toMillis('2018-03-27', '[Y1]-[M01]-[D01]')"))
    assert.are.equal(1522159380123, run("$toMillis('2018-03-27T14:03:00.123Z', '[Y0001]-[M01]-[D01]T[H01]:[m01]:[s01].[f001]Z')"))
    assert.are.equal(1517443200000, run("$toMillis('201802', '[Y0001][M01]')"))
    assert.are.equal(1517788800000, run("$toMillis('20180205', '[Y0001][M01][D01]')"))
  end)
  it("ordinal + roman + letters + words + names", function()
    assert.are.equal(196732800000, run("$toMillis('27th 3 1976', '[D1o] [M#1] [Y0001]')"))
    assert.are.equal(-2777932800000, run("$toMillis('21st 12 1881', '[D1o] [M01] [Y0001]')"))
    assert.are.equal(441763200000, run("$toMillis('MCMLXXXIV', '[YI]')"))
    assert.are.equal(1522108800000, run("$toMillis('27 iii MMXVIII', '[D1] [Mi] [YI]')"))
    assert.are.equal(1521763200000, run("$toMillis('w C mmxviii', '[Da] [MA] [Yi]')"))
    assert.are.equal(441763200000, run("$toMillis('one thousand, nine hundred and eighty-four', '[Yw]')"))
    assert.are.equal(1209254400000, run("$toMillis('27th April 2008', '[D1o] [MNn] [Y0001]')"))
    assert.are.equal(1328140800000, run("$toMillis('2 Feb 2012', '[D1] [MNn,3-3] [Y0001]')"))
  end)
  it("day-of-year (dateB) + 12h/24h time", function()
    assert.are.equal(1522800000000, run("$toMillis('2018-094', '[Y0001]-[d001]')"))
    assert.are.equal(1522800360000, run("$toMillis('4/4/2018 12:06 am', '[D1]/[M1]/[Y0001] [h]:[m] [P]')"))
    assert.are.equal(1522843560000, run("$toMillis('4/4/2018 12:06 pm', '[D1]/[M1]/[Y0001] [h]:[m] [P]')"))
  end)
  it("timezone offsets", function()
    assert.are.equal("2020-09-09T06:00:00.000Z", run("$fromMillis($toMillis('2020-09-09 08:00:00 +02:00', '[Y0001]-[M01]-[D01] [H01]:[m01]:[s01] [Z01:01]'))"))
    assert.are.equal(
      "2020-09-09T13:00:00.000Z",
      run("$fromMillis($toMillis('2020-09-09 08:00:00 GMT-05:00', '[Y0001]-[M01]-[D01] [H01]:[m01]:[s01] [z01:01]'))")
    )
  end)
  it("no match -> undefined; errors", function()
    assert.is_nil(run("$toMillis('irrelevent string', '[Y]-[M]-[D]')"))
    local ok, e = pcall(run, "$toMillis('2018-05-22', '[Y]-[M]-[q]')")
    assert.is_false(ok)
    assert.are.equal("D3132", e.code)
    local ok2, e2 = pcall(run, "$toMillis('2018-05-22', '[YN]-[M]-[D]')")
    assert.is_false(ok2)
    assert.are.equal("D3133", e2.code)
    local ok3, e3 = pcall(run, "$toMillis('2018-22', '[Y]-[D]')")
    assert.is_false(ok3)
    assert.are.equal("D3136", e3.code)
    local ok4, e4 = pcall(run, "$toMillis('2018-32-5', '[X]-[W]-[F1]')")
    assert.is_false(ok4)
    assert.are.equal("D3136", e4.code)
  end)
  it("2-digit year remap matches JS Date.UTC (0-99 -> 1900-1999)", function()
    assert.are.equal(-2177452800000, run("$toMillis('1', '[Y1]')"))
    assert.are.equal(915148800000, run("$toMillis('99', '[Y1]')"))
    assert.are.equal(-315619200000, run("$toMillis('60', '[Y1]')"))
    assert.are.equal(-315619200000, run("$toMillis('sixty', '[Yw]')"))
    assert.are.equal(929404800000, run("$toMillis('0099-06-15', '[Y0001]-[M01]-[D01]')"))
    -- year >= 100 is literal, NOT remapped
    assert.are.equal(-59011459200000, run("$toMillis('100', '[Y1]')"))
  end)
end)
